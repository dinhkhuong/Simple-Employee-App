    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // get the criteria from the JTextField
        String fnc = jTextField1.getText();
        String lnc = jTextField2.getText();
        String adc = jTextField3.getText();
        String pnc = jTextField4.getText();
        //reset the model for the JTable
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //reset the Jtable each time the SEARCH button clicked
        model.setRowCount(0);
        String f = "",l = "",a = "",p ="";
        Connection conn;
        try {
            conn = DriverManager.getConnection("jdbc:derby://localhost:1527/AccountDB", "khuong", "123");
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("select * from EMPLOYEES where FIRSTNAME like '%"+fnc+"%' "
                    + "and LASTNAME like '%"+lnc+"%' and ADDRESS like '%"+adc+"%' and PHONENUMBER like '%"+pnc+"%'");
            while (rs.next()){
                f = rs.getString(1);
                l = rs.getString(2);
                a = rs.getString(3);
                p = rs.getString(4);
                //get these string variables which contains the filled values 
                //stick them to the JTable
                model.insertRow(jTable1.getRowCount(), new Object[] {f, l, a, p});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Search.class.getName()).log(Level.SEVERE, null, ex);
        }               
    }//GEN-LAST:event_jButton2ActionPerformed
    
