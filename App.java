    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection conn;
        try {
            conn = DriverManager.getConnection("jdbc:derby://localhost:1527/AccountDB", "khuong", "123");
            //Use the insert query 
            String query = "insert into EMPLOYEES (firstname, lastname, address, phonenumber) values (?,?,?,?)";
            PreparedStatement pstmt = conn.prepareStatement(query);
            //1,2,3,4 is the index of the columns counting after the ID key attribute
            pstmt.setString(1, jTextField1.getText()); 
            pstmt.setString(2, jTextField2.getText()); 
            pstmt.setString(3, jTextField3.getText());
            pstmt.setString(4, jTextField4.getText());
            pstmt.executeUpdate();
                        
        } catch (SQLException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

